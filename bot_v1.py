async def check_subscription(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user = update.message.from_user
    chat_member = await context.bot.get_chat_member(f"@{CHANNEL_USERNAME}", user.id)
    if chat_member.status in ['left', 'kicked']:
        keyboard = [[InlineKeyboardButton("⚠️ اشترك في القناة ⚠️", url=f"https://t.me/{CHANNEL_USERNAME}")]]
        reply_markup = InlineKeyboardMarkup(keyboard)
        await update.message.reply_text("𖤐 يجب عليك الاشتراك في القناة أولاً!", reply_markup=reply_markup)
        return False
    return True


async def warn_user(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not await check_subscription(update, context):
        return

    user_id = await ensure_user_or_reply(update, context)
    if user_id:
        chat_id = update.message.chat.id
        warnings.setdefault(chat_id, {}).setdefault(user_id, 0)
        warnings[chat_id][user_id] += 1
        if warnings[chat_id][user_id] >= 3:
            await context.bot.restrict_chat_member(chat_id, user_id, ChatPermissions(can_send_messages=False))
            await update.message.reply_text(f"𖤐 تم كتم المستخدم {user_id} بعد ثلاث تحذيرات.⏳")
        else:
            await update.message.reply_text(f"تحذير {warnings[chat_id][user_id]}/3 للمستخدم.")

